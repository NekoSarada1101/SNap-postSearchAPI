import json
import os

import sqlalchemy
from flask import Flask, request, make_response

# If `entrypoint` is not defined in app.yaml, App Engine will look for an app
# called `app` in `main.py`.
app = Flask(__name__)


@app.route('/')
def hello():
    """Return a friendly HTTP greeting."""
    response_data = {"body": "hello", "status": "200"}
    return make_response(json.dumps(response_data))


@app.route('/search')
def searchPost():
    word = request.args.get('word')
    db = init_unix_connection_engine()
    response_data = fetch_search_post(db, word)

    return make_response(json.dumps(response_data))


def init_unix_connection_engine():
    db_user = os.environ["DB_USER"]
    db_pass = os.environ["DB_PASS"]
    db_name = os.environ["DB_NAME"]
    db_socket_dir = os.environ.get("DB_SOCKET_DIR", "/cloudsql")
    cloud_sql_connection_name = os.environ["CLOUD_SQL_CONNECTION_NAME"]

    pool = sqlalchemy.create_engine(
        # Equivalent URL:
        # mysql+pymysql://<db_user>:<db_pass>@/<db_name>?unix_socket=<socket_path>/<cloud_sql_instance_name>
        sqlalchemy.engine.url.URL(
            drivername="mysql+pymysql",
            username=db_user,  # e.g. "my-database-user"
            password=db_pass,  # e.g. "my-database-password"
            database=db_name,  # e.g. "my-database-name"
            query={
                "unix_socket": "{}/{}".format(
                    db_socket_dir,  # e.g. "/cloudsql"
                    cloud_sql_connection_name)  # i.e "<PROJECT-NAME>:<INSTANCE-REGION>:<INSTANCE-NAME>"
            }
        ),
        # ... Specify additional properties here.
    )
    return pool


def fetch_search_post(db, word):
    with db.connect() as conn:
        result = conn.execute("SELECT * FROM posts WHERE message LIKE %s", "%" + word + "%").fetchall()
        data_list = []
        for data in result:
            data_list.append({"id": data[0], "message": data[1]})
        response_data = {
            "data": data_list,
            "status": 200
        }
        return response_data


